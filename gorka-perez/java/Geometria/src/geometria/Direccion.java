package geometria;

public class Direccion {
	private int arriba;
	private int abajo;
	private int derecha;
	private int izquierda;

	public Direccion(int arriba, int abajo, int derecha, int izquierda)
	{
		this.arriba = arriba;
		this.abajo = abajo;
		this.derecha = derecha;
		this.izquierda = izquierda;
	}

}
