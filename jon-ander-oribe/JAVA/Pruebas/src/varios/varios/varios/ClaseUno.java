package varios;

public class ClaseUno {
	
	protected  int m1=4;
	protected  int m2=5;
	protected  int m3=6;
	
	public ClaseUno(int m1,int m2,int m3){
		this.m1=m1;
		this.m2=m2;
		this.m3=m3;
	}
	
	public int getM1(){
		return m1;
	}
	public  int getM2(){
		return m2;
	}
	public  int getM3(){
		return m3;
	}

}
